package com.dbeginc.dbshopping.base

import android.support.v7.util.DiffUtil

/**
 * Created by darel on 06.03.18.
 *
 * Generic data difference calculator
 */
abstract class BaseDataDiff<out T>(protected val oldData: List<T>, protected val newData: List<T>): DiffUtil.Callback() {
    override fun getOldListSize(): Int = oldData.size

    override fun getNewListSize(): Int = newData.size

    override fun areContentsTheSame(oldItemPosition: Int, newItemPosition: Int): Boolean = oldData[oldItemPosition] == newData[newItemPosition]
}